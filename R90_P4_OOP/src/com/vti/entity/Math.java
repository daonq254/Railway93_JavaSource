package com.vti.entity;

public class Math {
//	Tính tổng 2 số nguyên
	public int sum(int num1, int num2) {
//		
		return num1 + num2;
	}

//	Tính tổng 2 số thực

	public float sum(float num1, float num2) {
		return num1 + num2;
	}

//	Tính tổng 3 số nguyên
	public int sum(int num1, int num2, int num3) {
		return num1 + num2 + num3;
	}
}
